public class DividedBy3And5 {

    public static void main(String[] args) {

        for (int i=1;i<=100;i++){       //for loop for number 1 to 100

            if (i%3==0){                //if condition to check if the number can be divided by 3

                System.out.println(i);  // print the value of i if condition in line 7 is true

            }

        }
        for (int j=1; j<=100;j++)        //for loop for number 1 to 100

            if (j%5==0){                //if condition to check if the number can be divided by 5

                System.out.println(j);  // print the value of i if condition in line 16 is true
            }


    }

}
